    public int[] calcularSorte(int[] sorteados, int[] valores){
        int ajuda=0;
        if(sorte>=100){
            ajuda=6;
            sorte-=100;
        }else if(sorte>=70){
            ajuda=3;
            sorte-=70;
        }else if(sorte>=30){
            ajuda=1;
            sorte-=30;
        }
        int aux=0, strike=0;
        while(ajuda>0){
            if(sorteados[aux]==valores[0])strike++;
            if(sorteados[aux]==valores[1])strike++;
            if(sorteados[aux]==valores[2])strike++;
            if(sorteados[aux]==valores[3])strike++;
            if(sorteados[aux]==valores[4])strike++;
            if(sorteados[aux]==valores[5])strike++;
            aux++;
        }
        return sorteados;
    }
    
    public void calcularPremio(int strike){
        double valorAposta = Double.valueOf(txtValorAposta.getText());
        double ApostaFracionada = valorAposta/100;
        double premio=0;
        switch(strike){
            case 0:premio = ApostaFracionada*0;break;
            case 1:premio = ApostaFracionada*25;break;
            case 2:premio = ApostaFracionada*50;break;
            case 3:premio = ApostaFracionada*100;break;
            case 4:premio = ApostaFracionada*200;break;
            case 5:premio = ApostaFracionada*500;break;
            case 6:premio = ApostaFracionada*1000;break;
        }
        lblPremio.setText("VOCÃŠ GANHOU: R$ " + premio +"(" + strike + ") ACERTOS");
        jogador.setDinheiro(jogador.getDinheiro()+premio);
        lblSaldo.setText("Saldo: R$ " + String.valueOf(jogador.getDinheiro()));
        telaAnterior.atualizarVetores(jogador, sorte, null, null, null, null);
        telaAnterior.alterarHUB();
    }
    
